local function getColorPalette(t)
	if not t then t = term.current() end
	local palette = {}
	for i = 0, 15 do
		local c = 2^i
		local r, g, b = t.getPaletteColor(c)
		palette[c] = colors.packRGB(r, g, b)
		print("Color", c, "rgb hex", palette[c], r, g, b)
	end
	return palette
end

