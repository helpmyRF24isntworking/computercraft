-- L1 = 42.5   a1 = 23.6   b1 = -4.4
-- L2 = 45.2   a2 = 18.5   b2 = 3.5
-- CIE ΔE00 = 6.4300403439 (Bruce Lindbloom, Netflix’s VMAF, ...)
-- CIE ΔE00 = 6.4300224381 (Gaurav Sharma, OpenJDK, ...)
-- Deviation between implementations ≈ 1.8e-5

-- See the source code comments for easy switching between these two widely used ΔE*00 implementation variants.

local function deltaEFromRGB(r1, g1, b1, r2, g2, b2)
    -- Convert RGB to LAB
    local l1, a1, b1 = rgbToLab(r1, g1, b1)
    local l2, a2, b2 = rgbToLab(r2, g2, b2)

    -- Calculate Delta E using CIEDE2000
    return ciede_2000(l1, a1, b1, l2, a2, b2)
end



