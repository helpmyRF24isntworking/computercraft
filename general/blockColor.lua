-- Function to convert RGB to CIE-LAB
local function rgbToLab(r, g, b)
    -- Normalize RGB values to [0, 1] already done by cc:t
    --r = r / 255
    --g = g / 255
    --b = b / 255

    -- Apply gamma correction
    local function gammaCorrect(value)
        if value > 0.04045 then
            return ((value + 0.055) / 1.055) ^ 2.4
        else
            return value / 12.92
        end
    end

    r = gammaCorrect(r)
    g = gammaCorrect(g)
    b = gammaCorrect(b)

    -- Convert to XYZ color space
    local x = r * 0.4124564 + g * 0.3575761 + b * 0.1804375
    local y = r * 0.2126729 + g * 0.7151522 + b * 0.0721750
    local z = r * 0.0193339 + g * 0.1191920 + b * 0.9503041

    -- Normalize for D65 illuminant
    x = x / 0.95047
    y = y / 1.00000
    z = z / 1.08883

    -- Convert to LAB
    local function f(value)
        if value > 0.008856 then
            return value ^ (1 / 3)
        else
            return (7.787 * value) + (16 / 116)
        end
    end

    local l = (116 * f(y)) - 16
    local a = 500 * (f(x) - f(y))
    local b = 200 * (f(y) - f(z))

    return l, a, b
end

-- Function to calculate Delta E (CIE76)
local function deltaE(r1, g1, b1, r2, g2, b2)
    -- Convert both colors to LAB
    local l1, a1, b1 = rgbToLab(r1, g1, b1)
    local l2, a2, b2 = rgbToLab(r2, g2, b2)

    -- Calculate the Euclidean distance in LAB space
    local deltaL = l1 - l2
    local deltaA = a1 - a2
    local deltaB = b1 - b2

    return math.sqrt(deltaL^2 + deltaA^2 + deltaB^2)
end



